	@Override
	public void createEntity() {
		OrderTransDetail orderTransDetail = new OrderTransDetail();
		setEntity(orderTransDetail);
	}

	@Override
	public void createEntityList() {
		setEntityList(new ArrayList<OrderTransDetail>());
	}

	@Override
	public void clean() {
		setEntity(new OrderTransDetail());
		getEntityList().clear();
	}

	@Override
	public void setEid(Integer eid) {
		setEntity(orderTransDetailService.getEntityById(eid));
		this.eid = eid;
	}

	@PostConstruct
	public void init() {
		initBase();
	}

	public void search() {
		setMessage(null);
		setEntityList(this.orderTransDetailService.list(getEntity()));
	}

	public void save(Integer id) {
		try {
			if (id != null) {
				this.orderTransDetailService.update(getEntity());
				setMessage("OrderTransDetail is successfully updated");
				clean();
			} else {
				this.orderTransDetailService.add(getEntity());
				setMessage("OrderTransDetail is successfully created");
				clean();
			}
		} catch (Exception e) {
			logger.error(e);
			setMessage(e.getMessage());
		}
	}

	public void remove(int row) {
		try {
			this.orderTransDetailService.remove(getEntityList().get(row).getId());
			setMessage("OrderTransDetail with id: "
					+ getEntityList().get(row).getId()
					+ " is succesfully deleted");
			getEntityList().remove(row);
		} catch (Exception e) {
			setMessage(e.getMessage());
		}
	}

	public String edit(int row) {
		setEntity(this.orderTransDetailService.getEntityById(getEntityList()
				.get(row).getId()));
		return "OrderTransDetail?faces-redirect=true&entityId="
				+ getEntityList().get(row).getId();
	}

	public String newEntity() {
		setMessage(null);
		setEntity(new OrderTransDetail());
		return "OrderTransDetail?faces-redirect=true";
	}

	public IOrderTransDetailService getOrderTransDetailService() {
		return orderTransDetailService;
	}

	public void setOrderTransDetailService(IOrderTransDetailService orderTransDetailService) {
		this.orderTransDetailService = orderTransDetailService;
	}




	public String addStockDepot(String pageName) {
		DataBean dataBean = new DataBean();
		if(FacesContext.getCurrentInstance()
		.getExternalContext().getFlash().get("obj")!=null){
			dataBean = (DataBean) FacesContext.getCurrentInstance().getExternalContext().getFlash().get("obj");
		}
		dataBean.setObj(getEntity());
		dataBean.setName(getEntity().getClass().getName());
		dataBean.setFrom(pageName);
		dataBean.setRelationType(RelationType.manyToOne);
		
		DataBean childDataBean = new DataBean();
		childDataBean.setFrom("StockDepotList");
		childDataBean.setName("org.demo.bean.yeni.StockDepot");
		childDataBean.setParentDataBean(dataBean);

		FacesContext.getCurrentInstance().getExternalContext().getFlash()
				.put("obj", childDataBean);
		return "StockDepotList?faces-redirect=true&addSelect=" + AddSelect.ADD.ordinal();
	}



